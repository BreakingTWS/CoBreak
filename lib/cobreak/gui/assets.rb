      def self.load_image_file(path)
        begin
          pixbuf = GdkPixbuf::Pixbuf.new(file: path)
          # Scale the image to a reasonable size if needed (e.g., 200x200)
          if pixbuf.width > 200 || pixbuf.height > 200
            ratio = [200.0 / pixbuf.width, 200.0 / pixbuf.height].min
            new_width = (pixbuf.width * ratio).to_i
            new_height = (pixbuf.height * ratio).to_i
            pixbuf = pixbuf.scale_simple(new_width, new_height, GdkPixbuf::InterpType::BILINEAR)
          end
          return pixbuf
        rescue => e
          puts "Error loading image: #{e.message}"
          return nil
        end
      end

